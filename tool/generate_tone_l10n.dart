  );
  _expandArb(
    root,
    path: 'lib/l10n/app_es.arb',
    locale: 'es',
    overrides: const {}, // Spanish: triple from existing Spanish neutral text
  );
  _writeResolver(root, enPath: 'lib/l10n/app_en.arb');
  stdout.writeln('Done. Run: flutter gen-l10n');
